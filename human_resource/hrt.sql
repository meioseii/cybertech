CREATE TABLE registration (

  id INT AUTO_INCREMENT PRIMARY KEY,
  
  surname TEXT,
  firstname TEXT,
  middlename TEXT,
  birthday TEXT,
  birthplace TEXT,
  gender TEXT,
  contact TEXT,
  email TEXT,
  socialMedia TEXT,
  civilStatus TEXT,
  educBackground TEXT,
  educAttainment TEXT,
  language TEXT,
  religion TEXT,
  jobExperience TEXT,
  homeAddress TEXT NULL 
  
);
